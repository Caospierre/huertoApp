class Message {
  String title;
  String body;
  String user;
  String numberphone;
  String image;
  String description;
  Message(title, body, user, numberphone, image, description) {
    this.title = title;
    this.body = body;
    this.user = user;
    this.numberphone = numberphone;
    this.image = image;
    this.description = description;
  }
}